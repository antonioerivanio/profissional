CREATE TABLE FP_AUXILIOSAUDEREQ(
    ID NUMBER(10) CONSTRAINT NN_FP_AUXILIOSAUDEREQ NOT NULL,
    IDFUNCIONAL NUMBER(10),    
    IDUSUARIO NUMBER(6),
    ID_PESSOAJURIDICA NUMBER(6),
    VALOR_PLANOSAUDE NUMBER(5,2),
    DT_INICIOREQ DATE  CONSTRAINT DT_INICIOREQ NOT NULL,
    DT_FIMREQ DATE  CONSTRAINT DT_FIMREQ NOT NULL,
    ID_DEPENDENTES NUMBER(6), 
    FL_VALIDADO CHAR(1) DEFAULT 0,
    OBSERVACAO VARCHAR2(500),
    
    CONSTRAINT "PK_AUXILIOSAUDEREQ" PRIMARY KEY ("ID"),    
    CONSTRAINT "FK_AUXILIOSAUDEREQ_FUNCIONAL" FOREIGN KEY ("IDFUNCIONAL")
    REFERENCES "SRH"."TB_FUNCIONAL" ("ID"),
    
    CONSTRAINT "FK_DEPENDENTES" FOREIGN KEY ("ID_DEPENDENTES")
    REFERENCES "SRH"."TB_FUNCIONAL" ("ID"),
    CONSTRAINT "CK_FL_VALIDADO" CHECK (FL_VALIDADO IN (0,1))
);

CREATE SEQUENCE  "SRH"."SEQ_AUXILIOSAUDEREQ"  MINVALUE 1 MAXVALUE 999999 INCREMENT BY 1 START WITH 1 NOCACHE  NOORDER  NOCYCLE ;

CREATE TABLE FP_AUXILIOSAUDEDOC(
    ID NUMBER(10) CONSTRAINT NN_FB_AUXILIOSAUDEDOC NOT NULL,
    ID_AUXILIOSAUDEREQ NUMBER(10),    
    CAMINHO_ARQ VARCHAR(200),
    NOME_ARQ VARCHAR(200),
    DT_INCLUSAO DATE,
    FL_DELETADO CHAR(1) DEFAULT 0,
    
    CONSTRAINT "PK_AUXILIOSAUDEDOC" PRIMARY KEY ("ID"),    
    CONSTRAINT "FK_AUXILIOSAUDEREQ" FOREIGN KEY ("ID_AUXILIOSAUDEREQ")
    REFERENCES "SRH"."TB_FUNCIONAL" ("ID"),
    CONSTRAINT "CK_FL_DELETADO" CHECK(FL_DELETADO IN (0,1))    
);

CREATE SEQUENCE  "SRH"."SEQ_FP_AUXILIOSAUDEDOC"  MINVALUE 1 MAXVALUE 999999 INCREMENT BY 1 START WITH 1 NOCACHE  NOORDER  NOCYCLE ;